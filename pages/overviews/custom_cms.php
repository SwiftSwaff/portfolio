        <h1>Custom CMS</h1>
        <p>This is an ongoing project of mine. It is a lightweight responsive CMS that is capable of being custom tailored to the needs of the user. Currently it is configured to be used by a lacrosse association, but can easily be adjusted programmatically depending on the client.</p>
        <p>The goal of this project was to make an alternative to WordPress that would score highly on performance metrics. In the process I learned various techniques to reduce page load times: the use of .webp format, pipelining of assets, preloading and caching resources, just to name a few.</p>
        <p>While I had envisioned for this project to do more, it is serviceable in its current state. It supports news posts as well as several features necessary for a sports association (standings, schedules, divisions, teams, and rosters) all of which are managed through a MySQL database.</p>
        <p>Technologies: PHP, MySQL, JavaScript, HTML5, CSS3 (with SASS)</p>
